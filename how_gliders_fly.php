            <div class="sub_topic">
                <h3>Staying Airborne</h3>
                <hr>
                <div>
                    So you’ve just launched a glider off a hill, you might be wondering whether you have to go and collect it 
                    from the bottom of the hill to have another go? Luckily, there are ways for gliders to stay airborne for hours, or 
                    more in a lot of cases. The atmosphere around us is never stationary, air moves both horizontally (as wind) and vertically. 
                    If we can position the glider in the upwards moving air, and the air is moving upwards faster than the glider is sinking 
                    downwards, we can gain height. Think of it as a person trying to walk down a fast escalator: if the escalator is moving fast 
                    enough, the person will be taken upwards, despite the fact that they are walking down it.
                        <br>
                        <br>
                    There are 3 basic types of lift that gliders use to stay airborne…

                    <div style="padding-left: 20px;">
                        <h4>Ridge Lift</h4>
                        <div>
                            <div class="img_container">
                                <img src="./media/ridge.png" alt="Ridge lift" >
                            </div>
                            Ridge lift is the easiest to imagine – the wind blows against the face of a hill (or a cliff), 
                            and the hill deflects the air upwards. The glider can fly in this upward moving air to gain height or stay 
                            at the same height. The closer to the hill you are, the stronger the lift will be. Even light winds of around 10 mph 
                            can be enough to keep a glider airborne, depending on the hill and the skill of the pilot
                        </div>
                    </div>

                    <div style="padding-left: 20px;">
                        <h4>Thermal Lift</h4>
                        <div>
                            <div class="img_container">
                                <img src="./media/thermal.png" alt="Thermal lift" >
                            </div>
                            In the summer (or even in spring and autumn when the sun is shining) different areas of the ground will 
                            heat up in the sun faster than others (for example, dark fields). These warmer areas heat up the air around them, 
                            and this air eventually breaks away from the ground as a bubble of warmer, rising air. These are called thermals, 
                            and gliders can use them to gain height on good days. Since they are roughly cylindrical in shape, gliders will turn 
                            in reasonably tight circles in order to stay in them. On a summer’s day, you can often see gliders turning in 
                            circles inside thermals to gain height.
                            <br>
                            <br>
                            As the air rises, it cools down, and eventually it will reach a height where the water vapour inside the air 
                            will condense, and form a cumulus cloud. These are the clouds that glider pilots look for in order to find where 
                            the thermals are located. The white, fluffy clouds you see on a summer’s day are all indicators that there is a 
                            thermal underneath them.
                        </div>
                    </div>

