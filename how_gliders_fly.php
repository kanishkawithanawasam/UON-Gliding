            <div class="sub_topic">
                <h3>Landing</h3>
                <hr>
                <div>
                    Eventually of course, you are going to want to land. Since gliders are so efficient, it is actually harder than 
                    you think to get them to come down! A typical training glider will travel about 30-35ft forwards for every foot 
                    in height it loses – equivalent to a glide angle of less than 2 degrees. For this reason, most gliders are fitted 
                    with airbrakes which help to destroy some of the lift and allow the glider to descend at a faster rate. The amount of 
                    airbrake used will effect how steep the approach is and how quickly the aircraft descends. The pilot can therefore use 
                    them to pretty much land the glider exactly where they want.
                    <br>
                    <br>
                    The final phase of the landing is the “round out” or the “flare”. This involves gently pitching the aircraft 
                    upwards just before it touches down in order to reduce the vertical speed and make for a nice smooth touch down. At 
                    first, you may find this very challenging to get right, however over time you will naturally get a feel for how to handle 
                    the aircraft in this last phase of flight.
