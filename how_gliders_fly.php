            <div class="sub_topic">
                <h3>Controlling a Glider</h3>
                <hr>
                <div>
                The attitude (how far up or down the nose points) and therefore speed of the glider is controlled by the elevator, 
                which is the control surface on the horizontal tailplane. This is controlled by the forward and backward movement of 
                the control stick in the cockpit. Pushing the control stick forward moves the elevator downwards, lifting the tail and 
                lowering the nose. This will increase the speed. Pulling back on the stick does the reverse, pointing the nose upwards and 
                slows the glider down.
                <br>
                <br>
                Just like a powered aircraft, gliders can be turned in the air with the ailerons, which are the control surface on each 
                of the wings. Moving the control stick to the left raises the right wing and lowers the left wing, rolling the glider to 
                the left. Moving the stick to the right does the opposite, and rolls the glider right.
                <br>
                <br>
                The last primary control is the rudder, controlled by the pilot’s feet. The rudder changes 
                the direction that the glider is pointing on the vertical axis, called the 
                yaw. Turning is achieved with the ailerons and rudder being used in combination with each other.
                </div>
            </div>

            <div class="sub_topic">
                <h3>Launching</h3>
                <hr>
                <div>
                Being un-powered, gliders need to get airborne somehow. There are 2 main launching methods we use, 
                and some other methods that are used at other sites, which we may visit on an expedition.
                <div style="padding-left: 20px;">
                    <h4>Winch Launching</h4>
                    <div>
                         <div class="img_container">
                            <img src="./media/winch-launch.jpeg" alt="Winch launching a glider" srcset="https://www.camgliding.uk/wp-content/uploads/2019/03/Picture1.jpg">
                        </div>
                        Perhaps the most common method of launching gliders is by winch launch. 
                        The idea is that at the opposite end of the airfield, there is a large winch, with a 
                        cable running all the way back along the field to the glider. The winch has a very powerful engine, 
                        capable of accelerating the glider from a standing start to 60mph in as little as 2 seconds! The glider’s 
                        wings start producing lift, and the pilot can then point the glider upwards at a 45 degree angle, rapidly gaining height. 
                        A winch launch can get you to 1,000ft in as little as 10 seconds on some days. There isn’t much in the world that will do this 
                        quicker on the right day, not even the Space Shuttle!
                        <br>
                        <br>
                        After a launch to typically about 1,300ft (but sometimes over 2,000ft when it is windy), 
                        the pilot releases the cable and continues the flight from there, hopefully catching a thermal to gain even more height.
                        <br>
                        <br>
                        Winch launches are popular because they are cheap, quick and can achieve a high launch 
                        rate if operated efficiently
                    </div>

                    <h4>Aerotowing</h4>
                    <div>
                        <div class="img_container">
                            <img src="./media/aerotow.jpeg" alt="aerotowing a glider">
                        </div>
                        Gliders are also commonly launched by aerotow. The glider is attached to a powered aircraft with a rope, 
                        the powered aircraft then takes off and climbs, dragging the glider along with it. Once the glider is at an 
                        altitude and position they are happy with, they simply release the rope and fly away, the aeroplane (called the “tug”) 
                        then returns to the airfield ready to collect the next customer.
                        <br>
                        <br>
                        The advantage of aerotowing is that it allows the pilot to choose where they get dropped off (for example, under a nice 
                        thermal!), and at what height the launch is taken to. They are more expensive than winch launches, and have a slower launch 
                        rate. Using several tow planes at once can achieve a good launch rate though, with most competitions using this launch method.
                    </div>

                    <h4>Other Launching Methods</h4>
                    <ul>
                    <li>Auto tow – The glider is attached to a car by a length of rope, which then drives down the runway, 
                        giving the glider air speed and allowing it to fly
                    </li>
                    <li>
                        Self Launching – This one applies to motor-gliders, and is fairly self-explanatory – the motor glider 
                        launches using its own propeller to give it air speed
                    </li>
                    <li>
                    Bungee – The glider is pulled down a hill with the aid of a bungee rope, until it has enough speed to fly. 
                    The bungee rope is then released, leaving the glider to fly
                    </li>
                </ul>
