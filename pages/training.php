            <div class="sub_topic">
                <h3>Your Pre-Solo Training</h3>
                <hr>
                <div>
                The aim of your pre-solo training is twofold. First of all, the aim is for you to understand how the glider and its 
                controls work so that you are able to control the glider during all stages of the flight, and also to avoid and deal with 
                any unusual situations that you could find yourself in.
                <br>
                <br>
                Secondly, the instructors aim to teach you how to fly safely. This means that you should be able to avoid any situations, 
                which could potentially be dangerous, as well as teaching you how to recover from these. You will learn about and practice 
                things like stalls, spins, spiral dives and launch failures.  If you are making good progress and the weather is on your side, 
                you should be able to go solo after 40 to 80 flights. But this is just an estimate – some students are able to do it in less 
                time and some need more time. However, there is still much more to learn beyond going solo…
                </div>
