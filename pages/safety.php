            <article>
                <h3>The Launch Point</h3>

                <div style="margin-left:50px;text-align:justify;">
                    <p >
                    The launch point is generally where gliders both launch and land. They will land on both sides of the bus, and are 
                    pretty quiet. If you need to cross the airfield, you should ask someone, until you know how the airfield operates 
                    enough to get safely across yourself. Gliders will normally circuit around the airfield to land, but they can approach 
                    from anywhere in some circumstances. You should check that there are no gliders on approach to land before you cross the 
                    field.
                    <br>
                    <br>
                    If you are half way across the field and you notice a glider landing, stand still. The pilot will have seen you, 
                    and there is nothing worse than trying to avoid someone who is running around trying to avoid you! That said, if it 
                    is too late for the pilot to do anything, or it still looks like they are flying towards you, get out of the way quickly!
                    <br>
                    <br>

                    When driving on the airfield, the same rules apply – you should always check for gliders and planes landing before 
                    crossing the landing area. Aircraft always have right of way over ground vehicles, even aircraft moving around on 
                    the ground.
                    <br>
                    <br>

                    Although gliders will normally land at the launch point, they can land anywhere on the field, so you should keep a good 
                    lookout wherever on the field you are.
                    <br>
                    <br>

                    As well as winch launching, we also aerotow at Cranwell, using a motorglider. Both of these have propellers on the 
                    front, which you should stay well clear of when they are operating. Never hang anything off a propeller on the ground, 
                    or attempt to spin it, as it may start if the controls in the cockpit have been left in the wrong position.
                    </p>  
                </div>  
                
            </article>


            <article>
                <h3>The Launch Point</h3>

                <div style="margin-left:50px;text-align:justify;">
                    <p>
                    The speed limit on the airfield for club vehicles is 20mph – this is to avoid breaking the springs, which are expensive and 
                    time consuming to replace. Slow down even further for the bumpier parts of the field.
                    <br>
                    <br>
                    Never leave the canopy of a glider open and unattended – on a windy day it can slam shut and smash. Even on a calm day, 
                    the tug or something driving by can cause a gust of wind, and it is good to get into the habit of doing it anyway!
                    <br>
                    <br>
                    Be careful with the parachutes. Always do the leg straps up before the chest strap – again, on a windy day, if you 
                    accidentally deploy the parachute, you won’t want to be dragged down the airfield by your neck. Never put a parachute on 
                    the ground – they can absorb moisture which may damage the chute. There is a place on the bus you can put unneeded parachutes. 
                    Treat them with a bit of respect – it is almost unheard of for one to be used, but they are a piece of life saving equipment 
                    nonetheless.
                    </p>  
