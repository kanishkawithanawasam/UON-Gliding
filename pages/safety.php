                            even if it’s not the cable being launched because you can’t guarantee the winch driver has selected the right cable.
                            <br>
                            <br>
                            If you are at the winch end of the field, keep well clear of the winch while a launch is in progress – it is recommended 
                            you stand at least 15m away, or get inside a vehicle. The cables can land in unexpected places if the glider pilot releases 
