                            One of the main methods of launch is a winch launch, which uses a very powerful winch and long steel cables to accelerate a 
                            glider from 0 to 60 mph in less than two seconds. These steel cables (and the glider attached to them) pose a very significant 
                            hazard to anyone on the ground.
                            <br>
                            <br>
                            There is a warning buzzer on the bus when launching is taking place, and the lights on the front of the bus will be flashing 
